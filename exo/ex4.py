liste_nombres = [100, 12, 69, 23, 89, 83, 56, 78, 91, 10]

maximum = max(liste_nombres)
print("Le maximum de la liste est :", maximum)


minimum = min(liste_nombres)
print("Le minimum de la liste est :", minimum)


moyenne = sum(liste_nombres) / len(liste_nombres)
print("La moyenne de la liste est :", moyenne)